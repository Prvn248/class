package day1;

public class RelationalOperator {
	public static void main(String[]args) {
	int num1=50;
	int num2=40;
	System.out.println(num2<num1);
	System.out.println(num2>num1);
	System.out.println(num2<=num1);
	System.out.println(num2>num1);
	System.out.println(num2=num1);
	System.out.println(num2!=num1);
}	

}
